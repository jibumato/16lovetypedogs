def build_faq_html(code):
    items = FAQ[code]
    inner = ''
    for q, a in items:
        inner += f'    <h3>Q. {q}</h3>\n    <p>A. {a}</p>\n'
    return f'''
  <div class="card">
    <h2>よくある恋愛Q&A</h2>
{inner}  </div>
'''

def build_faqpage_schema(code, type_url):
    items = FAQ[code]
    entries = []
    for q, a in items:
        entries.append({
            "@type": "Question",
            "name": q,
            "acceptedAnswer": {"@type": "Answer", "text": a}
        })
    return {
        "@type": "FAQPage",
        "mainEntity": entries
    }

